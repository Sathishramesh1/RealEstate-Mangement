import mongoose from "mongoose";

const PropertySchema = new mongoose.Schema({
    propertyType: {
        type: String,
        required: true
    },
    location: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

const Property = mongoose.model("Property", PropertySchema);

export { Property };
